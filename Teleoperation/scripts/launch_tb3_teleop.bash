Control Your TurtleBot3!
---------------------------
Moving around:
        w
   a    s    d
        x

w/x : increase/decrease linear velocity 
(Burger : ~ 0.22, Waffle Pi : ~ 0.26)
a/d : increase/decrease angular velocity 
(Burger : ~ 2.84, Waffle Pi : ~ 1.82)

space key, s : force stop

CTRL-C to quit

